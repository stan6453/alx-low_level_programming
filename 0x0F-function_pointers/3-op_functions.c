
/**
 * op_add - adds two digits
 * @a: 1st digit to adds
 * @b: 2nd digit to adds
 * Description: adds two digits
 * Return: the result of the addition
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two digits
 * @a: digit to be subtracted from
 * @b: digit to be subtracted
 * Description: subtracts b from a two digits
 * Return: the result of the subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two digits
 * @a: 1st digit in the multiplication
 * @b: 2nd digit to be multiplied
 * Description: multiplies two digits
 * Return: the result of the multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two digits
 * @a: digit to be divided
 * @b: digit to be divided by
 * Description: divides a by b
 * Return: the result of the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - divides two digits and returns the remainder
 * @a: digit to be divided
 * @b: digit to be divided by
 * Description: divides a by b and returns the remainder
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
